void concurrent_exec(unsigned cur_round) {
    if (cur_round <= E3_rounds) {
        if (E3_concurrency == 32) {
            for(unsigned i = 0; i < E3_concurrency; i++) {
                random_orders_32[E3_param_start_offset + cur_round - 1][i] == 0 ?
                    e3_exec_query_1(cur_round * E3_concurrency + i) :
                    e3_exec_query_9(cur_round * E3_concurrency + i);
            }
        } else if (E3_concurrency == 64) {
            for(unsigned i = 0; i < E3_concurrency; i++) {
                random_orders_64[E3_param_start_offset + cur_round - 1][i] == 0 ?
                    e3_exec_query_1(cur_round * E3_concurrency + i) :
                    e3_exec_query_9(cur_round * E3_concurrency + i);
            }
        } else if (E3_concurrency == 128) {
            for(unsigned i = 0; i < E3_concurrency; i++) {
                random_orders_128[E3_param_start_offset + cur_round - 1][i] == 0 ?
                    e3_exec_query_1(cur_round * E3_concurrency + i) :
                    e3_exec_query_9(cur_round * E3_concurrency + i);
            }
        }
    }
}

seastar::future<> run_e3_workload() {
    return seastar::repeat([] {
        auto round_id = e3_helper.current_round();
        concurrent_exec(round_id);

        return seastar::repeat([round_id] {
            if (e3_helper.current_round() == round_id) {
                return seastar::sleep(std::chrono::seconds(1)).then([] {
                    return seastar::stop_iteration::no;
                });
            }
            return seastar::make_ready_future<seastar::stop_iteration>(seastar::stop_iteration::yes);
        }).then([] {
            return e3_helper.current_round() <= E3_rounds ?
                seastar::stop_iteration::no : seastar::stop_iteration::yes;
        });
    });
}

